GLOSSARY = {

    # ━━━━━━━━━━━━━━━━━━━━━━━━━━━
    # 宏观经济学
    # ━━━━━━━━━━━━━━━━━━━━━━━━━━━

    "GDP (Gross Domestic Product, 国内生产总值)": """
        一个国家在一段时间内生产的所有商品和服务的总价值.
        类比: 把全国所有人赚的钱加起来, 就是GDP.
        GDP增长快 = 经济繁荣, GDP增长慢/负增长 = 经济衰退.
        中国2024年GDP约126万亿元, 增速约5%.
    """,

    "CPI (Consumer Price Index, 消费者价格指数)": """
        衡量"物价涨了多少"的指标.
        CPI上涨 = 通货膨胀(钱变得不值钱了).
        CPI > 3%: 通胀偏高, 央行可能加息
        CPI < 0%: 通缩(物价下跌), 经济可能有问题
        与投资的关系: 你的投资收益率必须跑赢CPI, 否则实际上在亏钱.
    """,

    "通货膨胀 (Inflation)": """
        物价持续上涨, 钱的购买力下降.
        例: 去年100元能买10斤猪肉, 今年只能买8斤 = 通胀20%.
        温和通胀(2-3%) = 经济健康的标志
        恶性通胀(>10%) = 经济出问题了
        与量化的关系: 长期投资收益率必须跑赢通胀才有意义.
    """,

    "利率 (Interest Rate)": """
        借钱的成本 / 存钱的回报.
        央行基准利率: 所有利率的"锚", 影响整个经济.
        加息(利率上升):
          - 存款更有吸引力 -> 资金从股市流向银行
          - 企业借钱成本高 -> 扩张放缓 -> 股价承压
          - 通常股市下跌, 债券下跌
        降息(利率下降):
          - 存款没吸引力 -> 资金从银行流向股市
          - 企业借钱便宜 -> 扩张加速 -> 股价上涨
          - 通常股市上涨, 债券上涨
    """,

    "经济周期 (Business Cycle)": """
        经济像四季一样循环: 繁荣 -> 过热 -> 衰退 -> 萧条 -> 复苏 -> 繁荣
        不同阶段适合不同的投资策略:
          复苏期: 买股票(企业盈利开始恢复)
          繁荣期: 买商品/大宗(需求旺盛推高价格)
          衰退期: 买债券(避险, 利率下降债券涨)
          萧条期: 持有现金(等待机会)
        这就是"美林时钟"的核心逻辑.
    """,

    "美林时钟 (Merrill Lynch Investment Clock)": """
        美林证券提出的经济周期资产配置模型.
        核心思想: 不同经济阶段, 买不同的资产.

             经济过热          经济复苏
            (买商品)          (买股票)
               ↑                ↑
        高通胀 ─┼────────────────┼─ 低通胀
               ↓                ↓
            (买现金)          (买债券)
             经济滞胀          经济衰退

        量化应用: 根据GDP增速+CPI判断当前处于哪个阶段,
        自动调整股票/债券/商品/现金的配置比例.
    """,

    # ━━━━━━━━━━━━━━━━━━━━━━━━━━━
    # 资产类别
    # ━━━━━━━━━━━━━━━━━━━━━━━━━━━

    "股票 (Stock/Equity)": """
        买入一家公司的所有权份额.
        收益来源: 股价上涨(资本利得) + 分红(股息)
        风险: 高. 可能亏损50%以上, 也可能翻倍.
        特点: 长期收益最高, 但波动最大.
        年化收益参考: A股长期约8-12%, 美股约10-12%.
    """,

    "债券 (Bond)": """
        借钱给政府或企业, 到期还本付息.
        收益来源: 固定利息 + 债券价格波动
        风险: 低~中. 国债几乎无风险, 企业债有违约风险.
        特点: 收益稳定, 与股票通常负相关(股跌债涨).
        年化收益参考: 国债约3-4%, 企业债约5-7%.

        关键规律: 利率上升 -> 债券价格下跌
                  利率下降 -> 债券价格上涨
        (因为新债券利率更高/更低, 旧债券的吸引力就变了)
    """,

    "基金 (Fund)": """
        把很多人的钱集中起来, 交给专业基金经理投资.
        类型:
          指数基金: 跟踪指数(如沪深300), 被动投资, 费用低
          主动基金: 基金经理自己选股, 费用高, 收益不确定
          ETF: 在交易所买卖的指数基金, 像股票一样交易
          货币基金: 投资短期债券, 风险极低(如余额宝)
        第13-14周会深入学习基金投资.
    """,

    "黄金 (Gold)": """
        传统避险资产, "乱世买黄金".
        特点: 不产生利息/分红, 靠价格上涨赚钱.
        作用: 对冲通胀 + 地缘政治风险时的避险工具.
        与美元通常负相关: 美元跌 -> 黄金涨.
    """,

    # ━━━━━━━━━━━━━━━━━━━━━━━━━━━
    # 资产配置
    # ━━━━━━━━━━━━━━━━━━━━━━━━━━━

    "资产配置 (Asset Allocation)": """
        把钱分散到不同类型的资产(股票/债券/黄金/现金)中.
        核心原则: "不要把鸡蛋放在一个篮子里"
        为什么有效? 不同资产涨跌不同步, 一个跌了另一个可能涨,
        整体组合的波动就小了, 但长期收益不一定低.
        这是投资中唯一的"免费午餐".
    """,

    "全天候策略 (All Weather Strategy)": """
        桥水基金(全球最大对冲基金)创始人达利奥提出的配置方案.
        核心思想: 不预测未来, 而是构建一个在任何经济环境下都能表现不错的组合.
        经典配比:
          股票 30% + 长期国债 40% + 中期国债 15% + 黄金 7.5% + 大宗商品 7.5%
        特点: 收益不是最高的, 但回撤很小, 非常稳健.
    """,

    "夏普比率 (Sharpe Ratio)": """
        衡量"每承担一单位风险, 能获得多少超额收益"的指标.
        公式: Sharpe = (组合收益率 - 无风险利率) / 组合波动率
        Sharpe > 1: 优秀
        Sharpe > 2: 非常优秀
        Sharpe < 0: 还不如存银行
        这是量化基金最看重的指标之一.
    """,

    "最大回撤 (Maximum Drawdown)": """
        从历史最高点到最低点的最大跌幅.
        例: 净值从100涨到150, 然后跌到120, 最大回撤 = (150-120)/150 = 20%
        这是衡量"最坏情况你会亏多少"的指标.
        回撤30%: 需要涨43%才能回本
        回撤50%: 需要涨100%才能回本(翻倍!)
        所以控制回撤比追求收益更重要.
    """,

    "相关性 (Correlation)": """
        两个资产涨跌的同步程度, 范围-1到+1.
        +1: 完全同涨同跌(没有分散效果)
         0: 完全无关(理想的分散对象)
        -1: 完全反向(一个涨另一个跌, 最好的对冲)
        资产配置的核心: 选择相关性低的资产组合在一起.
    """,
}

